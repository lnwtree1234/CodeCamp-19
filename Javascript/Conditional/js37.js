let company = prompt('Which company created JavaScript?');
company == 'Netscape' ? alert('Right!') : alert('Wrong.');

// สามารถทำงานได้ 
// แสดงผลลัพธ์ว่า "Right!" หรือ "Wrong." ตามคำตอบที่ผู้ใช้กรอก
// ถ้าใช่ (true) เมื่อ คำตอบ == 'Netscape' จะเรียก alert('Right!')
// ถ้าไม่ใช่ (false) คำตอบ != 'Netscape' จะเรียก alert('Wrong.')
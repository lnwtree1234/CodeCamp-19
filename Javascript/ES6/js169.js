const q = { prop: 5, prop2: [10, 100] };

const { prop: x, prop2: [, y] } = q;

console.log(x); // 5
console.log(y); // 100

/*
prop: x ใช้เพื่อดึงค่าของ prop จากออบเจ็กต์ q และกำหนดให้ตัวแปร x มีค่าเป็น 5
prop2: [, y] ใช้เพื่อดึงค่าจาก prop2 ซึ่งเป็นอาเรย์ โดย [,] ใช้เพื่อข้ามค่าตำแหน่งที่ 0 และกำหนดให้ตำแหน่งที่ 1 ดังนั้นค่า 100 จะถูกเก็บในตัวแปร y
*/